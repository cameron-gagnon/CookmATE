class ChocCookies:
    TEST_INGREDIENTS = []
    TEST_INGREDIENTS.append("3/4 cup granulated sugar")
    TEST_INGREDIENTS.append("3/4 cup packed brown sugar")
    TEST_INGREDIENTS.append("1 cup butter or margarine, softened")
    TEST_INGREDIENTS.append("1 teaspoon vanilla")
    TEST_INGREDIENTS.append("1 large egg")
    TEST_INGREDIENTS.append("2 1/4 cups all-purpose flour")
    TEST_INGREDIENTS.append("1 teaspoon baking soda")
    TEST_INGREDIENTS.append("1/2 teaspoon salt")
    TEST_INGREDIENTS.append("1 bag (12oz) semisweet chocolate chips (2 cups)")


    TEST_STEPS = []
    TEST_STEPS.append("Heat oven to 375 degrees F (175 degrees C)")
    TEST_STEPS.append("In large bowl, beat sugars, butter, vanilla and egg with " +
                    "electric mixer on medium speed, or mix with spoon. Stir in " +
                    "flour, baking soda and salt (dough will be stiff). Stir in " +
                    "nuts and chocolate chips.")
    TEST_STEPS.append("On an ungreased cookie sheet, drop dough by rounded tablespoons " +
                    "about 2 inches apart.")
    TEST_STEPS.append("Bake 8 to 10 minutes or until light brown (centers will be " +
                    "soft). Cool 1 to 2 minutes; remove from cookie sheet wire rack.")

    TEST_PREP = "PT10M"
    TEST_BAKE = "PT9M"
    TEST_TOTAL = "PT20M"
    TEST_NAME = "Chocolate Chip Cookies"
    NUTRITION_INFO = "275"
    OVEN_TEMP = 375
    
class SnowCones:
    TEST_INGREDIENTS = []
    TEST_INGREDIENTS.append("3/4 cup granulated snow")
    TEST_INGREDIENTS.append("1 snow cone")
    TEST_INGREDIENTS.append("7 sprits of water")
    TEST_INGREDIENTS.append("1/4 cup packed yellow snow")

    TEST_STEPS = []
    TEST_STEPS.append("Heat oven to 105 F to warm hands.")
    TEST_STEPS.append("In large bowl, mix the various snows "+
                      "with your hands on medium speed for one minute.")
    TEST_STEPS.append("In snow cone cups, drop snow by round balls, about 2 inches" +
                      " in diameter.")
    TEST_STEPS.append("Let warm 2 to 3 minutes or until slightly runny. Enjoy!")

    TEST_PREP = "PT2M"
    TEST_BAKE = "PT3M"
    TEST_TOTAL = "PT6M"
    TEST_NAME = "Snow Cones"
    NUTRITION_INFO = "0"
    OVEN_TEMP = 105
